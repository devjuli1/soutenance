                    <h1 class="mb-4">
                        Pas de santé, sans santé mentale                       
                    </h1>
                    <p class="fs-4 fw-normal">La santé mentale est une composante de notre santé aussi importante que la santé physique. « Il n’y pas de santé sans santé mentale », rappelle l’Organisation mondiale de la santé (OMS).
                       Pour définir la santé au sens large, l’OMS énumère ses trois dimensions indispensables : « La santé est un état de complet bien-être physique, mental et social, et ne consiste pas seulement en une absence de maladie ou d’infirmité.»
                    </p>
                    <h1 class="mb-4">
                        Du bien-être au mal-être, et vice-versa 
                    </h1>
                    <p class=" fs-4 fw-normal bg-light">Il n’existe pas une manière unique de définir la santé mentale. Elle correspond à l’état psychologique ou émotionnel de la personne, à un moment donné.
                       La santé mentale, une affaire d'équilibre; Chaque personne est en permanence à la recherche d’un équilibre entre les ressources qu’elle peut mobiliser pour se sentir bien, et les obstacles qu’elle rencontre dans son existence. Son état varie du bien-être au mal-être, et vice-versa. Cela, tout au long de la vie.
                       Ainsi l’équilibre peut être rompu, puis rétabli. Par exemple, je suis lassée de l’emploi que j’occupe. Cela me mine. Je ne souhaite pas démissionner, car la sécurité financière est importante pour moi. On m’apprend que je peux m’inscrire à des cours du soir et ainsi, changer de voie. Cette perspective me porte et je retrouve de l’énergie.
                       Pour rétablir l’équilibre, on peut trouver des ressources autour de soi, comme ces cours du soir. On peut aussi les trouver en soi-même. Par exemple, je viens de vivre une rupture amoureuse et n’ai plus envie de voir personne. Je trouve du réconfort en pratiquant, seul, la course à pied. Bientôt je me sens mieux et prêt à reprendre les sorties avec mon groupe d’amis.
                       Pour l’OMS, la santé mentale est « un état de bien-être dans lequel une personne peut se réaliser, surmonter les tensions normales de la vie, accomplir un travail productif et contribuer à la vie de sa communauté ». Cette définition propose une vision positive de la santé mentale. Néanmoins, elle pourrait laisser penser – à tort – que sans emploi, une personne ne peut pas connaître un état de bien-être.
                    </p>
                    <h1 class="mb-4">
                      Notre entourage influence notre santé mentale
                    </h1>
                    <p class="fs-4 fw-normal "> Notre santé mentale se consolide ou se détériore en fonction de ce qui se passe autour de nous et aussi, de ce qui nous arrive. Il y a des événements éprouvants sur lesquels nous n’avons pas prise, par exemple quand nous traversons un deuil. Il y a aussi des situations que nous ne pouvons pas changer à nous seuls, par exemple si nous ne trouvons pas de logement en raison de nos revenus, ou si nous subissons le sexisme dans notre métier. Nous ne pouvons pas tout mais… nous pouvons essayer d’agir sur notre manière de faire face aux difficultés quand elles se présentent.
                       Nous pouvons par exemple améliorer notre capacité à gérer le stress. Ainsi, un événement négatif aura peut-être moins d’impact sur notre bien-être. Nous pouvons aussi apprendre à communiquer plus efficacement, gagner en habileté dans nos relations avec les autres, faire travailler notre imagination pour résoudre les problèmes que nous rencontrons. Ces compétences dites « psychosociales » se développent dans l’enfance. Pourtant, on peut les renforcer tout au long de notre vie d’adulte. Et elles peuvent nous aider à garder notre équilibre face à l’adversité. 
                       Pour cultiver ses propres ressources, on peut s’appuyer sur des conseils pratiques, des exercices et des techniques en accès libre sur internet. On peut aussi utiliser des applis gratuites sur son téléphone.
                    </p>
